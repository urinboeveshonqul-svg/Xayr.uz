/* ===== STATE ===== */
let currentLang = 'uz';
let currentFilter = 'all';
let currentDonateId = null;

/* ===== LANGUAGE ===== */
function setLang(lang) {
  currentLang = lang;
  document.querySelectorAll('.lang-btn').forEach(btn => btn.classList.remove('active'));
  document.querySelectorAll('.lang-btn').forEach(btn => {
    if (btn.textContent.toLowerCase() === lang) btn.classList.add('active');
  });
  applyTranslations();
  renderCampaigns();
}

function t(key) {
  return i18n[currentLang][key] || i18n['uz'][key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  const searchInput = document.getElementById('searchInput');
  if (searchInput) searchInput.placeholder = t('search_placeholder');
}

/* ===== RENDER CAMPAIGNS ===== */
function renderCampaigns(filter, query) {
  const grid = document.getElementById('campaignsGrid');
  if (!grid) return;

  const activeFilter = filter !== undefined ? filter : currentFilter;
  const searchQuery = query !== undefined ? query : (document.getElementById('searchInput')?.value || '');

  let list = campaigns.filter(c => {
    const matchCat = activeFilter === 'all' || c.category === activeFilter;
    const title = c['title_' + currentLang] || c.title_uz;
    const matchSearch = !searchQuery || title.toLowerCase().includes(searchQuery.toLowerCase());
    return matchCat && matchSearch;
  });

  if (list.length === 0) {
    grid.innerHTML = `<div class="empty-state"><span>🔍</span><p>${t('empty')}</p></div>`;
    return;
  }

  grid.innerHTML = list.map(c => {
    const pct = Math.min(100, Math.round((c.raised / c.goal) * 100));
    const title = c['title_' + currentLang] || c.title_uz;
    const desc = c['desc_' + currentLang] || c.desc_uz;
    const raised = formatMoney(c.raised);
    const goal = formatMoney(c.goal);
    return `
      <div class="campaign-card" data-category="${c.category}">
        <div class="card-image" style="background:${c.color}">
          <span style="font-size:72px">${c.emoji}</span>
          <span class="card-badge">${t('cat_' + catKey(c.category))}</span>
          ${c.urgent ? `<span class="card-urgent">${t('urgent')}</span>` : ''}
        </div>
        <div class="card-body">
          <div class="card-organizer">👤 ${c.organizer}</div>
          <h3 class="card-title">${title}</h3>
          <p class="card-desc">${desc}</p>
          <div class="progress-section">
            <div class="progress-bar"><div class="progress-fill" style="width:${pct}%"></div></div>
            <div class="progress-info">
              <span class="progress-raised">${raised} so'm</span>
              <span class="progress-goal">${pct}%</span>
            </div>
          </div>
          <div class="card-meta">
            <span>🎯 ${goal} so'm</span>
            <span>👥 ${c.donors} ${t('donors')}</span>
            <span>⏰ ${c.days} ${t('days_left')}</span>
          </div>
          <div class="card-actions">
            <button class="btn-donate" onclick="openDonate(${c.id})">${t('btn_donate')}</button>
            <button class="btn-share" onclick="shareCampaign(${c.id})" title="${t('btn_share')}">🔗</button>
          </div>
        </div>
      </div>`;
  }).join('');
}

function catKey(cat) {
  const map = { medical: 'medical', education: 'edu', disaster: 'disaster', community: 'community', environment: 'env' };
  return map[cat] || cat;
}

function formatMoney(n) {
  return n.toLocaleString('uz-UZ');
}

/* ===== FILTER & SEARCH ===== */
function filterCampaigns(cat) {
  currentFilter = cat;
  document.querySelectorAll('.cat-btn').forEach(btn => btn.classList.remove('active'));
  event.target.classList.add('active');
  renderCampaigns(cat);
}

function searchCampaigns() {
  const q = document.getElementById('searchInput').value;
  renderCampaigns(currentFilter, q);
}

/* ===== MODALS ===== */
function openModal(type) {
  document.getElementById(type + 'Modal').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closeModal(type) {
  document.getElementById(type + 'Modal').classList.remove('open');
  document.body.style.overflow = '';
}

function closeModalOutside(e) {
  if (e.target === e.currentTarget) {
    e.currentTarget.classList.remove('open');
    document.body.style.overflow = '';
  }
}

/* ===== DONATE ===== */
function openDonate(id) {
  currentDonateId = id;
  const c = campaigns.find(x => x.id === id);
  if (!c) return;
  const title = c['title_' + currentLang] || c.title_uz;
  document.getElementById('donateTarget').innerHTML = `💚 ${title}`;
  openModal('donate');
}

function setAmount(val) {
  document.getElementById('donateAmount').value = val;
  document.querySelectorAll('.preset-btn').forEach(btn => {
    btn.classList.toggle('selected', parseInt(btn.textContent.replace(/,/g, '')) === val);
  });
}

function submitDonation(e) {
  e.preventDefault();
  const amount = parseInt(document.getElementById('donateAmount').value);
  if (!amount || amount < 1000) return;
  const c = campaigns.find(x => x.id === currentDonateId);
  if (c) {
    c.raised = Math.min(c.goal, c.raised + amount);
    c.donors += 1;
  }
  closeModal('donate');
  document.getElementById('donateForm').reset();
  document.querySelectorAll('.preset-btn').forEach(b => b.classList.remove('selected'));
  renderCampaigns();
  showToast(t('toast_donate'), 'success');
}

